package PerlWM::Widget::Button;

############################################################################

use strict;
use warnings;

use base qw(PerlWM::Widget::Label);

############################################################################

sub EVENT {
  (__PACKAGE__->SUPER::EVENT(),
   'Click(Button1)' => 'onClick');
}

############################################################################

sub onClick { 
  my($self, $event) = @_;
  if ($self->{action}) {
    $self->{action}->($self);
  }
}

############################################################################

1;
